    function get_viewport_size(){
        if(typeof(window.innerWidth) == 'number'){
            my_width = window.innerWidth;
            my_height = window.innerHeight;
        }else if(document.documentElement && (document.documentElement.clientWidth || document.documentElement.clientHeight)){
            my_width = document.documentElement.clientWidth;
            my_height = document.documentElement.clientHeight;
        }else if(document.body && (document.body.clientWidth || document.body.clientHeight)){
            my_width = document.body.clientWidth;
            my_height = document.body.clientHeight;
        }
        return {width: my_width, height: my_height};
    }
    
    function bannerHeight(selectorID){
        document.getElementById(selectorID).style.height = windowLength.height;
    }
    
    function setupControl() {
        var myVideo = document.getElementById("secVideo");
        var vele = document.getElementById("videoContainer");
        var buffer = document.getElementById("buffer");
        var bannerRow = document.getElementById("welcomeSec1row3");
        if (myVideo.canPlayType) {
            myVideo.removeAttribute("controls");
            
            // add event-handlers to control the video element
            myVideo.addEventListener("ended", function(){
                removeClass(vele, 'active');
                vele.style.background = 'transparent';
                myVideo.style.display = 'none';
                bannerRow.style.display = 'block';
            });
            
            // enable and disable the controls buttons to reflect the player state
            myVideo.addEventListener("play",function() {
                addClass(vele, 'active');
                myVideo.style.display = 'block';
                bannerRow.style.display = 'none';
                vele.style.background = '#000';
            }, false);
            
            myVideo.addEventListener("pause", function() {
                removeClass(vele, 'active');
                bannerRow.style.display = 'block';
            }, false);
            
            myVideo.addEventListener("loadstart", function() {
                if(hasClass(vele, 'active')){
                    buffer.style.display = 'block';
                }
            }, false);
            
            myVideo.addEventListener("loadeddata", function() {
                buffer.style.display = 'none';
            }, false);
            
            // add event-handlers for the control buttons
            document.getElementById("controlBtn").addEventListener("click",function(){
                if(!hasClass(vele, 'active')){
                    document.getElementById("secVideo").play();
                } else {
                    document.getElementById("secVideo").pause();
                }
            },false);
        }
    }
    
    function hasClass(el, className) {
        if (el.classList){   
            return el.classList.contains(className);
        } else {
            return !!el.className.match(new RegExp('(\\s|^)' + className + '(\\s|$)'));
        }
    }

    function addClass(el, className){
        if (el.classList) {
            el.classList.add(className)
        } else if (!hasClass(el, className)){
            el.className += " " + className 
        }
    }

    function removeClass(el, className) {
        if (el.classList) {
            el.classList.remove(className)
        } else if (hasClass(el, className)) {
            var reg = new RegExp('(\\s|^)' + className + '(\\s|$)')
            el.className=el.className.replace(reg, ' ')
        }
    }
    
    function timelineAnimation(){
        var controller = new ScrollMagic.Controller(), tween = new TimelineMax();
        $('path.orange_path').each(function(){
            var ele = $(this);
            tween.add(TweenMax.to(ele, 0.16, {strokeDashoffset: 0, ease:Linear.easeNone}))
        });
        var scene = new ScrollMagic.Scene({
            triggerElement: "#welcomeSec2",
            duration: $('#welcomeSec2').innerHeight() - window.innerHeight,
            offset: 350,
            tweenChanges: true}).setTween(tween).addTo(controller).on("end", function(){
                $('.curveType2').addClass('active');
            });
    }
    
    function secElementsAnimation(){
